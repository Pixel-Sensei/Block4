# Aufgabe 5 - Gleitkommazahlen

# Zwei Gleitkommazahlen werden definiert: a und b
a = 0.0002
b = 7e-4

# Die beiden Gleitkommazahlen werden ausgegeben
print(a)  # Ausgabe von a
print(b)  # Ausgabe von b

# Eine extrem kleine Gleitkommazahl wird direkt gedruckt
print(0.0000000000000000000000001)  # Ausgabe einer extrem kleinen Gleitkommazahl
