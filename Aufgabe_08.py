# Aufgabe 8 - Prioritäten

# Berechnung mit Standardprioritäten
print(5 * 25 % 8 + 100 / 2 * 8 // 2)

# Berechnung mit geänderten Prioritäten durch Klammern
print(5 * 25 % (8 + 100 / 2 * 8 // 2))

# Berechnung mit weiteren geänderten Prioritäten durch Klammern
print(5 * ((25 % 8) + 100) / 2 * 8 // 2)
